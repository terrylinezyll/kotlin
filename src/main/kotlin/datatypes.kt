fun main(args: Array<String>) {
    var num1:Float=34.7f //float datatype
    var num2:Double=34.90 //double datatype
    var num3:Byte=120
    var num4:Short=6745
    var num5:Long=2347558686969
    var num6:Int=32389
    var mystring:String="fill"
    var mychar:Char='c'
    var mybool:Boolean=true

    println("Float example is $num1")
    println("Double example is $num2")
    println("Byte example is $num3")
    println("Short example is $num4")
    println("Long example is $num5")
    println("Int example is $num6")
    println("String example is $mystring")
    println("Char example is $mychar")
    println("Boolean example is $mybool")
}